/** 列表视图需要的数据 */
export function getReceiptListItems(): ReceiptCertificateListItem[] {
  return MOCK_RECEIPT_CERTIFICATES.map(o => ({
    id: o.id,
    projectName: o.projectName,
    projectShortName: o.projectShortName,
    constructionUnit: o.constructionUnit,
    location: o.location,
    permitNumber: o.permitNumber,
    qualityNumber: o.qualityNumber,
    receiptRegistrationNo: o.receiptRegistrationNo,
    receiptNo: o.receiptNo,
    signStatus: o.signStatus,
    signedFileUrl: o.signStatus === 'signed' ? `/files/receipt-${o.id}.pdf` : undefined,
    fileCount: o.fileHistory?.length || 1,
    latestCreateTime: o.fileHistory?.[0]?.createTime || o.createTime,
  }));
}

/** 根据 ID 查找 */
export function getReceiptById(id: string): ReceiptCertificate | undefined {
  return MOCK_RECEIPT_CERTIFICATES.find(o => o.id === id);
}

/** 更新并返回 */
export function updateReceipt(updated: ReceiptCertificate): ReceiptCertificate {
  const idx = MOCK_RECEIPT_CERTIFICATES.findIndex(o => o.id === updated.id);
  if (idx >= 0) {
    MOCK_RECEIPT_CERTIFICATES[idx] = { ...updated, modifyTime: new Date().toISOString().replace('T', ' ').slice(0, 19) };
  }
  return MOCK_RECEIPT_CERTIFICATES[idx] || updated;
}

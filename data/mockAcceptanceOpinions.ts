/** 列表视图需要的数据 */
export function getOpinionListItems(): AcceptanceOpinionListItem[] {
  return MOCK_ACCEPTANCE_OPINIONS.map(o => ({
    id: o.id,
    projectName: o.projectName,
    projectShortName: o.projectShortName,
    constructionUnit: o.constructionUnit,
    location: o.location,
    permitNumber: o.permitNumber,
    qualityNumber: o.qualityNumber,
    receiptRegistrationNo: o.receiptRegistrationNo,
    signStatus: o.signStatus,
    signedFileUrl: o.signStatus === 'signed' ? `/files/opinion-${o.id}.pdf` : undefined,
    fileCount: o.fileHistory?.filter(f => f.signStatus === 'signed').length || 0,
    latestCreateTime: o.fileHistory?.[0]?.createTime || o.createTime,
  }));
}

/** 根据 ID 查找 */
export function getOpinionById(id: string): AcceptanceOpinion | undefined {
  return MOCK_ACCEPTANCE_OPINIONS.find(o => o.id === id);
}

/** 更新并返回 */
export function updateOpinion(updated: AcceptanceOpinion): AcceptanceOpinion {
  const idx = MOCK_ACCEPTANCE_OPINIONS.findIndex(o => o.id === updated.id);
  if (idx >= 0) {
    MOCK_ACCEPTANCE_OPINIONS[idx] = { ...updated, modifyTime: new Date().toISOString().replace('T', ' ').slice(0, 19) };
  }
  return MOCK_ACCEPTANCE_OPINIONS[idx] || updated;
}
